float map(float input, float imin, float imax, float omin, float omax){
  float output = 0.0f;
  if(input <= imin){
    output = omin;
  }
  else if(input >= imax){
    output = omax;
  }
  else{
    output = ((input - imin)/(imax - imin)) * (omax - omin) + omin;
  }
  return output;
}
